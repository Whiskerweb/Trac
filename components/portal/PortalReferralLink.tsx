'use client'

import { useState } from 'react'
import { useTranslations } from 'next-intl'
import { Copy, Check, Link2 } from 'lucide-react'

interface PortalReferralLinkProps {
    linkUrl: string
    primaryColor: string
}

export default function PortalReferralLink({ linkUrl, primaryColor }: PortalReferralLinkProps) {
    const t = useTranslations('portal.home')
    const [copied, setCopied] = useState(false)

    const handleCopy = () => {
        navigator.clipboard.writeText(linkUrl)
        setCopied(true)
        setTimeout(() => setCopied(false), 2000)
    }

    const shareOnTwitter = () => {
        window.open(`https://twitter.com/intent/tweet?url=${encodeURIComponent(linkUrl)}`, '_blank')
    }

    const shareOnLinkedIn = () => {
        window.open(`https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent(linkUrl)}`, '_blank')
    }

    return (
        <div className="bg-white rounded-2xl border border-gray-100 p-5 sm:p-6 card-hover">
            <div className="flex items-center gap-2 mb-3">
                <Link2 className="w-4 h-4 text-gray-400" />
                <p className="text-sm font-semibold text-gray-900">{t('yourReferralLink')}</p>
            </div>

            <div className="flex items-center gap-2">
                <div className="flex-1 bg-gray-50 rounded-xl px-4 py-3 overflow-hidden">
                    <code className="text-xs text-gray-700 break-all">{linkUrl}</code>
                </div>
                <button
                    onClick={handleCopy}
                    className="flex items-center gap-1.5 px-4 py-3 text-white rounded-xl text-xs font-semibold transition-all hover:opacity-90 btn-press flex-shrink-0"
                    style={{ backgroundColor: primaryColor }}
                >
                    {copied ? <Check className="w-3.5 h-3.5" /> : <Copy className="w-3.5 h-3.5" />}
                    {copied ? t('copied') : t('copy')}
                </button>
            </div>

            <div className="flex items-center gap-2 mt-3">
                <button
                    onClick={shareOnTwitter}
                    className="flex items-center gap-1.5 px-3 py-2 bg-gray-50 rounded-lg text-xs text-gray-600 hover:bg-gray-100 transition-colors btn-press"
                >
                    <svg className="w-3.5 h-3.5" viewBox="0 0 24 24" fill="currentColor"><path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" /></svg>
                    Twitter
                </button>
                <button
                    onClick={shareOnLinkedIn}
                    className="flex items-center gap-1.5 px-3 py-2 bg-gray-50 rounded-lg text-xs text-gray-600 hover:bg-gray-100 transition-colors btn-press"
                >
                    <svg className="w-3.5 h-3.5" viewBox="0 0 24 24" fill="currentColor"><path d="M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433a2.062 2.062 0 01-2.063-2.065 2.064 2.064 0 112.063 2.065zm1.782 13.019H3.555V9h3.564v11.452zM22.225 0H1.771C.792 0 0 .774 0 1.729v20.542C0 23.227.792 24 1.771 24h20.451C23.2 24 24 23.227 24 22.271V1.729C24 .774 23.2 0 22.222 0h.003z" /></svg>
                    LinkedIn
                </button>
            </div>
        </div>
    )
}
